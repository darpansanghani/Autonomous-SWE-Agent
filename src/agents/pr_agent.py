import re
from typing import List, Any

from src.llm.router import agent_completion
from src.github_client.issue_reader import GitHubIssue
from src.github_client.pr_creator import PRCreator
from src.github_client.repo_manager import RepoManager

PR_PROMPT = """Generate a professional Pull Request description.

ISSUE: #{issue_number} - {issue_title}

CHANGES:
{changes}

PLAN EXECUTED:
{plan}

Return ONLY the markdown body. Be concise. Include a "Summary" section, a "Changes Made" table, and "Closes #{issue_number}".
"""

def slugify(text: str) -> str:
    """Convert to branch-safe string."""
    text = re.sub(r'[^a-zA-Z0-9\s-]', '', text).strip().lower()
    return re.sub(r'[\s-]+', '-', text)

async def create_pull_request(
    repo_url: str,
    repo_path: str,
    issue: GitHubIssue,
    code_changes: List[Any],
    test_results: Any,
    plan: Any,
    total_cost: float
) -> str:
    """Orchestrates creating the branch, committing, pushing, and opening the PR."""
    
    manager = RepoManager()
    creator = PRCreator()
    
    # 1. Branch
    branch_name = f"agent/issue-{issue.number}-{slugify(issue.title)[:30]}"
    manager.create_branch(repo_path, branch_name)
    
    # 2. Assume patch is already applied to repo_path by `apply_patch_node`
    # We just need to commit it.
    manager.commit(repo_path, f"fix(#{issue.number}): {issue.title}\n\nAutomated commit by SWE Agent.")
    
    # 3. Push
    manager.push(repo_path, branch_name)
    
    # 4. Generate Body
    changes_str = "\n".join([f"- {c.action} {c.file_path}" for c in code_changes])
    messages = [
        {"role": "user", "content": PR_PROMPT.format(
            issue_number=issue.number,
            issue_title=issue.title,
            changes=changes_str,
            plan=plan.model_dump_json() if plan else ""
        )}
    ]
    
    response = await agent_completion("pr_writer", messages)
    pr_body = response.choices[0].message.content
    
    # Append stats
    pr_body += f"\n\n---\n*🤖 Generated by Autonomous SWE Agent • Cost: ${total_cost:.2f}*"
    
    # 5. Open PR
    pr_url = creator.create_pull_request(
        repo_url=repo_url,
        issue_number=issue.number,
        title=f"fix: {issue.title} [agent]",
        body=pr_body,
        head_branch=branch_name
    )
    
    return pr_url
